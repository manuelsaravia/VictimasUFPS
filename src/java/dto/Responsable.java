package dto;

/**
 *
 * @author Manuel
 */
public class Responsable {
    
    private String nombre;
    private String apellido;
    private String telefono;
    private String direccionResidencial;
    private String municipioResidencial; 
    private String cargoResponsable; 
    private String emailResponsable; 

    public Responsable() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCargoResponsable() {
        return cargoResponsable;
    }

    public void setCargoResponsable(String cargoResponsable) {
        this.cargoResponsable = cargoResponsable;
    }

    public String getEmailResponsable() {
        return emailResponsable;
    }

    public void setEmailResponsable(String emailResponsable) {
        this.emailResponsable = emailResponsable;
    }

    public String getDireccionResidencial() {
        return direccionResidencial;
    }

    public void setDireccionResidencial(String direccionResidencial) {
        this.direccionResidencial = direccionResidencial;
    }

    public String getMunicipioResidencial() {
        return municipioResidencial;
    }

    public void setMunicipioResidencial(String municipioResidencial) {
        this.municipioResidencial = municipioResidencial;
    }
    
    
    
}
